public class Video {
  private String url;
  private int duracion;

  public void setUrl(String url) {
    this.url = url;
  }
  public String getUrl() {
    return url;
  }
  public void setDuracion(int duracion) {
    this.duracion = duracion;
  }
  public int getDuracion() {
    return duracion;
  }
}
